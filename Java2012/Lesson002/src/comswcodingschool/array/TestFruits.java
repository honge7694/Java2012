package comswcodingschool.array;

public class TestFruits {
	private String fruitName;
	private int fruitSell;
	
	public TestFruits() {}
	
	public TestFruits(String fruitName, int fruitSell) {
		this.fruitName = fruitName;
		this.fruitSell = fruitSell;
	}

	public String getFruitName() {
		return fruitName;
	}

	public void setFruitName(String fruitName) {
		this.fruitName = fruitName;
	}

	public int getFruitSell() {
		return fruitSell;
	}

	public void setFruitSell(int fruitSell) {
		this.fruitSell = fruitSell;
	}
	
	
}
